<h2 id="russo-per-azienda">
    Russo per l’azienda
</h2>
<div class='course-description'>
<img src="/img/business.jpg" alt="business">
<p>Durante il corso di russo online oppure in sede a Bologna conoscerai le abitudini e il linguaggio degli interlocutori russi in campo aziendale, e potrai metterli in pratica in situazioni comunicative con partner e colleghi russofoni.</p>
<p>Potrai imparare:</p>
<ul>
  <li>Lessico aziendale;</li>
  <li>Corrispondenza commerciale;</li>
  <li>Relazioni commerciali con la Federazione Russa e i paesi russofoni;</li>
  <li>Particolarità della business culture russa;</li>
  <li>Simulazioni di trattative.</li>
</ul>
<p>Avrai a disposizione un materiale didattico completo e organizzato, che potrai consultare anche autonomamente.</p>
</div>

<h2 id="russo-per-preparazione-esami-universitari">
    Russo per preparazione esami universitari
</h2>
<div class='course-description'>
<img src="/img/russo-per-esami.jpg" alt="esami">
<p>Questo corso ti supporterà nella preparazione degli esami universitari di lingua russa, sia scritti sia orali.</p>
<p>I nostri insegnanti ti spiegheranno la grammatica russa in maniera chiara, logica e ben strutturata, proponendoti un materiale didattico adatto al tuo esame.</p>
<p>Ti aiuteremo anche a rafforzare la tua lingua parlata tramite attività di conversazione, e ti daremo un riscontro immediato sui tuoi progressi.</p>
<p>Tutti gli studenti che hanno seguito i nostri corsi di russo online oppure in sede a Bologna hanno superato con successo i loro esami, speriamo di poter aiutare anche te.</p>
</div>

<h2 id="russo-per-viaggiare">
    Russo per viaggiare
</h2>
<div class='course-description'>
<img src="/img/turismo.jpg" alt="turismo">
<p>Durante il corso di russo online oppure in sede a Bologna potrai imparare l'alfabeto cirillico, un vocabolario utile, e mettere in pratica le tue nuove conoscenze in situazioni comunicative di viaggio come:</p>
<ul>
    <li>Andare a mangiare fuori;</li>
    <li>Fare una prenotazione in un hotel;</li>
    <li>Chiedere informazioni sui prezzi;</li>
    <li>Fare compere;</li>
    <li>Viaggiare ed usare i mezzi pubblici ecc.</li>
</ul>
<p>Per rendere il tuo viaggio più interessante e piacevole il corso comprende anche nozioni culturali e geografiche, e tanti consigli pratici su come affrontare la quotidianità in Russia.</p>
</div>

<h2 id="russo-per-adozione">
    Russo per adozione
</h2>
<div class='course-description'>
<img src="/img/adozione.jpg" alt="adozione">
