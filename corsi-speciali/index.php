    <li>Comprendere meglio la personalità del bambino e comunicare con lui fin dai primi giorni.</li>
    <li>Capire come approcciarsi alla cultura russa in situazioni di vita quotidiana, e quindi come comportarsi durante il viaggio in Russia.</li>
</ul>
<p>Grazie all'esperienza acquisita negli anni, siamo riusciti a progettare un materiale didattico che permetta di raggiungere questi obiettivi in tempi brevi.</p>
