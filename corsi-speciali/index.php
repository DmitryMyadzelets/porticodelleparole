<section>
    <h1>
        Corsi di russo speciali
    </h1>

    <h2>
        <a name="russo-per-adozione">
            Russo per adozione
        </a>
    </h2>
    <div class='course-description'>
        <img src="/img/adozione.jpg">
        <p>
            Programma speciale per genitori che vogliono adottare un bambino russo e che desiderano:
            <ul>
                <li>Imparare le nozioni basi della lingua russa;</li>
                <li>Capire, conoscere meglio la personalità del bambino;</li>
                <li>Comunicare con lui dai primi giorni.</li>
            </ul>
        </p>
        <p>
            Inoltre, il corso permette ai nuovi genitori di imparare come comportarsi durante il loro 
            viaggio in Russia e come approcciarsi alla cultura russa nelle situazioni di vita 
            quotidiana (ristoranti, negozi, indicazioni stradali ecc.).
        </p>
        <p>
            Grazie all’esperienza acquisita negli anni, i materiali didattici sono pensati in modo tale 
            da assimilare in tempi brevi tutte le capacità necessarie per questi obiettivi.
        </p>
    </div>

    <h2>
        <a name="russo-per-preparazione-esami-universitari">
            Russo per preparazione esami universitari
        </a>
    </h2>
    <div class='course-description'>
        <img src="/img/russo-per-esami.jpg">
        <p>
            Questo corso ti permetterà di superare sia la parte scritta che quella orale degli 
            esami universitari di lingua russa. Avrai la possibilità di avere:
            <ul>
                <li>Lezioni e materiali adatti per spiegare la grammatica russa in maniera chiara, logica e ben strutturata;</li>
                <li>Conversazione pratica al fine di sviluppare le capacità comunicative dello studente.</li>
            </ul>
        </p>
        <p>
            Tutti gli studenti che hanno seguito i nostri corsi hanno superato con successo i loro esami. Ora tocca a te!
        </p>
    </div>

    <h2>
        <a name="russo-per-azienda">
            Russo per l’azienda
        </a>       
    </h2>
    <div class='course-description'>
        <img src="/img/business.jpg">
        <p>
            Fin dalle prime lezione avrai modo apprendere e comprendere:
            <ul>
                <li>Le relazioni commerciali con la Federazione Russa e i paesi russofoni;</li>
                <li>La terminologia aziendale;</li>
                <li>La corrispondenza commerciale.</li>
            </ul>
        </p>
        <p>
            Conoscerai, così, le particolarità del business culture russa, le abitudini e la lingua degli interlocutori, 
            così da poter comunicazione con partner e colleghi russofoni.
        </p>
        <p>
            Il tutto è costantemente accompagnato da materiali chiari e ben organizzati, 
            che potrai consultare facilmente anche autonomamente.
        </p>
    </div>

    <h2>
        <a name="russo-per-viaggiare">
            Russo per viaggiare
        </a>
    </h2>
    <div class='course-description'>
        <img src="/img/turismo.jpg">
        <p>
            Programma per imparare come usare la lingua russa nelle situazioni di viaggio più comuni.
            Impara il vocabolario di russo per le tue vacanze per:
            <ul>
                <li>Andare a mangiare fuori;</li>
                <li>Prenotare un hotel;</li>
                <li>Chiedere informazioni sui prezzi;</li>
                <li>Fare compere;</li>
                <li>Viaggiare ed usare i mezzi pubblici ecc.</li>
            </ul>
        </p>
        <p>
            Per rendere il tuo viaggio più interessante e piacevole il corso si basa anche su 
            informazioni utili riguardanti la cultura, la geografia e tanti consigli pratici su 
            come affrontare la vita di tutti i giorni in Russia.
        </p>
    </div>
